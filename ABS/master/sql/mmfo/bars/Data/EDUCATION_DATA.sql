MERGE INTO BARS.EDUCATION A USING
 (SELECT
  1 as ID,
  'початкова освіта' as NAME
  FROM DUAL) B
ON (A.ID = B.ID)
WHEN NOT MATCHED THEN 
INSERT (
  ID, NAME)
VALUES (
  B.ID, B.NAME)
WHEN MATCHED THEN
UPDATE SET 
  A.NAME = B.NAME;

