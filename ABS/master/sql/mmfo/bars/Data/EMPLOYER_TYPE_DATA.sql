MERGE INTO BARS.EMPLOYER_TYPE A USING
 (SELECT
  1 as ID,
  'Юридична особа' as NAME
  FROM DUAL) B
ON (A.ID = B.ID)
WHEN NOT MATCHED THEN 
INSERT (
  ID, NAME)
VALUES (
  B.ID, B.NAME)
WHEN MATCHED THEN
UPDATE SET 
  A.NAME = B.NAME;

