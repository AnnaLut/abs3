MERGE INTO BARS.BPK_TAGS A USING
 (SELECT
  'BUS_MOD' as TAG,
  'Тип бізнес моделі для договору' as NAME,
  NULL as TYPE
  FROM DUAL) B
ON (A.TAG = B.TAG)
WHEN NOT MATCHED THEN 
INSERT (
  TAG, NAME, TYPE)
VALUES (
  B.TAG, B.NAME, B.TYPE)
WHEN MATCHED THEN
UPDATE SET 
  A.NAME = B.NAME,
  A.TYPE = B.TYPE;

