CREATE OR REPLACE FORCE VIEW BARS.v_ob_corp_report
(
   CORPORATION_ID,
   FILE_DATE,
   ROWTYPE,
   KF,
   NLS,
   KV,
   OKPO,
   OBDB,
   OBDBQ,
   OBKR,
   OBKRQ,
   OST,
   OSTQ,
   KOD_CORP,
   KOD_USTAN,
   KOD_ANALYT,
   DAPP,
   POSTDAT,
   DOCDAT,
   VALDAT,
   ND,
   VOB,
   DK,
   MFOA,
   NLSA,
   KVA,
   NAMA,
   OKPOA,
   MFOB,
   NLSB,
   KVB,
   NAMB,
   OKPOB,
   S,
   DOCKV,
   SQ,
   NAZN,
   DOCTYPE,
   POSTTIME,
   NAMK,
   NMS,
   TT,
   SESSION_ID
)
AS
   select
a.corp_id as CORPORATION_ID, 
a.fdat as FILE_DATE, 
0 as ROWTYPE,
a.kf as KF, 
a.nls as NLS,
a.kv as KV,
a.okpo as OKPO,
a.obdb as OBDB,
a.obdbq as OBDBQ,
a.obkr as OBKR,
a.obkrq as OBKRQ, 
a.ost as OST, 
a.ostq as OSTQ, 
a.corp_id as KOD_CORP, 
a.kod_ustan as KOD_USTAN, 
a.kod_analyt as KOD_ANALYT, 
a.dapp as DAPP, 
a.postdat as POSTDAT,
null as DOCDAT, 
null as VALDAT,
null as ND,
null as VOB, 
null as DK, 
null as MFOA, 
null as NLSA, 
null as KVA, 
null as NAMA, 
null as OKPOA, 
null as MFOB, 
null as NLSB, 
null as KVB, 
null as NAMB, 
null as OKPOB, 
null as S,
null as DOCKV, 
null as SQ, 
null as NAZN, 
null as DOCTYPE, 
null as POSTTIME, 
a.namk as NAMK, 
a.nms as NMS,
null as TT, 
a.sess_id as SESSION_ID
from OB_CORP_DATA_ACC a 
where a.is_last = 1
union all
select
a.corp_id as CORPORATION_ID, 
a.fdat as FILE_DATE, 
1 as ROWTYPE,
a.kf as KF, 
a.nls as NLS,
a.kv as KV,
null as OKPO,
null as OBDB,
null as OBDBQ,
null as OBKR,
null as OBKRQ, 
null as OST, 
null as OSTQ, 
a.corp_id as KOD_CORP, 
a.kod_ustan as KOD_USTAN, 
a.kod_analyt as KOD_ANALYT, 
null as DAPP, 
d.postdat as POSTDAT,
d.docdat as DOCDAT, 
d.valdat as VALDAT,
d.nd as ND,
d.vob as VOB, 
1 as DK, 
d.mfoa as MFOA, 
d.nlsa as NLSA, 
d.kva as KVA, 
d.nama as NAMA, 
d.okpoa as OKPOA, 
d.mfob as MFOB, 
d.nlsb as NLSB, 
d.kvb as KVB, 
d.namb as NAMB, 
d.okpob as OKPOB, 
d.s as S,
d.dockv as DOCKV, 
d.sq as SQ, 
d.nazn as NAZN, 
d.dk as DOCTYPE, 
d.postdat as POSTTIME, 
a.namk as NAMK, 
a.nms as NMS,
d.tt as TT, 
a.sess_id as SESSION_ID
from OB_CORP_DATA_ACC a
join OB_CORP_DATA_DOC d on a.sess_id = d.sess_id and a.acc = d.acc and a.kf = d.kf
where a.is_last = 1
union all
select
a.corp_id as CORPORATION_ID, 
a.fdat as FILE_DATE, 
2 as ROWTYPE,
a.kf as KF, 
to_char(count(*)) as NLS,
null as KV,
null as OKPO,
null as OBDB,
null as OBDBQ,
null as OBKR,
null as OBKRQ, 
null as OST, 
null as OSTQ, 
a.corp_id as KOD_CORP, 
null as KOD_USTAN, 
null as KOD_ANALYT, 
null as DAPP, 
null as POSTDAT,
null as DOCDAT, 
null as VALDAT,
null as ND,
null as VOB, 
null as DK, 
null as MFOA, 
null as NLSA, 
null as KVA, 
null as NAMA, 
null as OKPOA, 
null as MFOB, 
null as NLSB, 
null as KVB, 
null as NAMB, 
null as OKPOB, 
null as S,
null as DOCKV, 
null as SQ, 
null as NAZN, 
null as DOCTYPE, 
null as POSTTIME, 
null as NAMK, 
null as NMS,
null as TT, 
a.sess_id as SESSION_ID
from OB_CORP_DATA_ACC a
where a.is_last = 1
group by a.corp_id, a.sess_id, a.fdat, a.kf;
/
  GRANT SELECT ON BARS.v_ob_corp_report TO CORP_CLIENT;
  GRANT SELECT ON BARS.v_ob_corp_report TO BARS_ACCESS_DEFROLE;
/