MERGE INTO BARS.SW_SYSTEM A USING
 (SELECT
  'st' as SYSTEMCODE,
  'ST' as SYSTEMNAME,
  'ST' as VEBSERVISE,
  NULL as CLOSE_DATE,
  '97' as KOD_NBU
  FROM DUAL) B
ON (A.SYSTEMCODE = B.SYSTEMCODE)
WHEN NOT MATCHED THEN 
INSERT (
  SYSTEMCODE, SYSTEMNAME, VEBSERVISE, CLOSE_DATE, KOD_NBU)
VALUES (
  B.SYSTEMCODE, B.SYSTEMNAME, B.VEBSERVISE, B.CLOSE_DATE, B.KOD_NBU)
WHEN MATCHED THEN
UPDATE SET 
  A.SYSTEMNAME = B.SYSTEMNAME,
  A.VEBSERVISE = B.VEBSERVISE,
  A.CLOSE_DATE = B.CLOSE_DATE,
  A.KOD_NBU = B.KOD_NBU;
