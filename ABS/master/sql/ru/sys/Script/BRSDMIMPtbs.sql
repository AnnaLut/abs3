prompt create tablespace BRSDMIMP

begin
	execute immediate q'[
		CREATE TABLESPACE BRSDMIMP DATAFILE 
		'&&DFILENAME' SIZE 128M AUTOEXTEND ON NEXT 128M MAXSIZE UNLIMITED,
		'&&DFILENAME' SIZE 128M AUTOEXTEND ON NEXT 128M MAXSIZE UNLIMITED,
		'&&DFILENAME' SIZE 128M AUTOEXTEND ON NEXT 128M MAXSIZE UNLIMITED,
		'&&DFILENAME' SIZE 128M AUTOEXTEND ON NEXT 128M MAXSIZE UNLIMITED,
		'&&DFILENAME' SIZE 128M AUTOEXTEND ON NEXT 128M MAXSIZE UNLIMITED
		LOGGING
		ONLINE
		EXTENT MANAGEMENT LOCAL AUTOALLOCATE
		SEGMENT SPACE MANAGEMENT AUTO]';
exception
	when others then
		if sqlcode = -1543 then null; else raise; end if;
end;
