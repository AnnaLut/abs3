  'Посилання на референс договору після реструктуризації' as NAME,
  NULL as TYPE
  FROM DUAL) B
ON (A.TAG = B.TAG)
WHEN NOT MATCHED THEN 
INSERT (
  TAG, NAME, TYPE)
VALUES (
  B.TAG, B.NAME, B.TYPE)
WHEN MATCHED THEN
