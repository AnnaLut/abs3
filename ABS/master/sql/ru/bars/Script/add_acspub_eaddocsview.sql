MERGE INTO BARS.OPERLIST_ACSPUB A USING
 (SELECT
  '/barsroot/usercontrols/dialogs/eadocsview.aspx\S*' as FUNCNAME,
  1 as FRONTEND
  FROM DUAL) B
ON (A.FUNCNAME = B.FUNCNAME and A.FRONTEND = B.FRONTEND)
WHEN NOT MATCHED THEN 
INSERT (
  FUNCNAME, FRONTEND)
VALUES (
  B.FUNCNAME, B.FRONTEND)
 ;

COMMIT;
