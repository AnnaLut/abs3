  'заміна активу (цінні папери), не пов’язана з реструктуризацією' as NAME_RESTR
  FROM DUAL) B
ON (A.VID_RESTR = B.VID_RESTR)
WHEN NOT MATCHED THEN 
INSERT (
  VID_RESTR, NAME_RESTR)
VALUES (
  B.VID_RESTR, B.NAME_RESTR)
