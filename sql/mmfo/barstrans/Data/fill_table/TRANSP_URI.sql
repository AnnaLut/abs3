  B.URI_ID, B.URI_DESC, B.SEND_TYPE, B.BASE_HOST, B.REQ_PATH, 
  B.CHK_PATH, B.AUTH_USER, B.AUTH_PATH, B.IS_ACTIVE, B.IS_LOCAL, 
  B.IS_DEFAULT, B.KF)
WHEN MATCHED THEN
UPDATE SET 
  A.URI_DESC = B.URI_DESC,
  A.SEND_TYPE = B.SEND_TYPE,
  A.BASE_HOST = B.BASE_HOST,
  A.REQ_PATH = B.REQ_PATH,
  A.CHK_PATH = B.CHK_PATH,
  A.AUTH_USER = B.AUTH_USER,
