MERGE INTO TRANSP_URI A USING
 (SELECT
  1 as URI_ID,
  'QQQ' as URI_DESC,
  1 as SEND_TYPE,
  'http://localhost:10032/barsroot/' as BASE_HOST,
  'api/transp/v1/direct' as REQ_PATH,
  'api/transp/v1/direct' as CHK_PATH,
  'ABSADM01' as AUTH_USER,
  'qwerty' as AUTH_PATH,
  1 as IS_ACTIVE,
  0 as IS_LOCAL,
  1 as IS_DEFAULT,
  300465 as KF
  FROM DUAL) B
ON (A.URI_ID = B.URI_ID)
WHEN NOT MATCHED THEN 
INSERT (
  URI_ID, URI_DESC, SEND_TYPE, BASE_HOST, REQ_PATH, 
  CHK_PATH, AUTH_USER, AUTH_PATH, IS_ACTIVE, IS_LOCAL, 
  IS_DEFAULT, KF)
