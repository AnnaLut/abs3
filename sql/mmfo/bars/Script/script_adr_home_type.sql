SET DEFINE OFF;
MERGE INTO BARS.ADR_HOME_TYPE A USING
 (SELECT
  1 as ID,
  'будинок' as NAME,
  'буд.' as VALUE,
  'building number' as NAME_ENG
  FROM DUAL) B
ON (A.ID = B.ID)
WHEN NOT MATCHED THEN 
INSERT (
  ID, NAME, VALUE, NAME_ENG)
VALUES (
  B.ID, B.NAME, B.VALUE, B.NAME_ENG)
WHEN MATCHED THEN
UPDATE SET 
  A.NAME = B.NAME,
  A.VALUE = B.VALUE,
  A.NAME_ENG = B.NAME_ENG;

