MERGE INTO BARS.META_FILTERCODES A USING
 (SELECT
  'NBU23_REZ_FILTER_CODE' as CODE,
  'Параметр для перегляду резерву по угоді ЦП' as NAME,
  '$~~ALIAS~~$.ND=:REF' as CONDITION,
  '/' as BRANCH
  FROM DUAL) B
ON (A.CODE = B.CODE)
WHEN NOT MATCHED THEN 
INSERT (
  CODE, NAME, CONDITION, BRANCH)
VALUES (
  B.CODE, B.NAME, B.CONDITION, B.BRANCH)
WHEN MATCHED THEN
UPDATE SET 
  A.NAME = B.NAME,
  A.CONDITION = B.CONDITION,
  A.BRANCH = B.BRANCH;

COMMIT;
