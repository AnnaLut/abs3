SET DEFINE OFF;

MERGE INTO BARS.CP_TAG A USING
 (SELECT
  3 as ID,
  'BUS_MOD' as TAG,
  'Тип бізнес моделі для договору' as NAME
  FROM DUAL) B
ON (A.TAG = B.TAG)
WHEN NOT MATCHED THEN 
INSERT (
  ID, TAG, NAME)
VALUES (
  B.ID, B.TAG, B.NAME)
WHEN MATCHED THEN
UPDATE SET 
  A.ID = B.ID,
  A.NAME = B.NAME;

