Begin
MERGE INTO BARS.FLR A USING
 (SELECT
  'Так' as FLR_ID,
  'Тип ставки FLOATING_RATE' as FLR_NAME
  FROM DUAL) B
ON (A.FLR_ID = B.FLR_ID)
WHEN NOT MATCHED THEN 
INSERT (
  FLR_ID, FLR_NAME)
VALUES (
  B.FLR_ID, B.FLR_NAME)
WHEN MATCHED THEN
UPDATE SET 
  A.FLR_NAME = B.FLR_NAME;
COMMIT;
end;
/
