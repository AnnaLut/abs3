SET DEFINE OFF;

MERGE INTO BARS.OPERLIST A USING
 (SELECT
  4481 as CODEOPER,
  'Way4. Портфель БПК(ЮО)' as NAME,
  'N/A' as DLGNAME,
  '/barsroot/way4bpk/way4bpk?custtype=2' as FUNCNAME,
  NULL as SEMANTIC,
  1 as RUNABLE,
  NULL as PARENTID,
  'OW' as ROLENAME,
  1 as FRONTEND,
  0 as USEARC
  FROM DUAL) B
ON (A.CODEOPER = B.CODEOPER)
WHEN NOT MATCHED THEN 
INSERT (
  CODEOPER, NAME, DLGNAME, FUNCNAME, SEMANTIC, 
  RUNABLE, PARENTID, ROLENAME, FRONTEND, USEARC)
VALUES (
  B.CODEOPER, B.NAME, B.DLGNAME, B.FUNCNAME, B.SEMANTIC, 
  B.RUNABLE, B.PARENTID, B.ROLENAME, B.FRONTEND, B.USEARC)
WHEN MATCHED THEN
UPDATE SET 
  A.NAME = B.NAME,
  A.DLGNAME = B.DLGNAME,
  A.FUNCNAME = B.FUNCNAME,
  A.SEMANTIC = B.SEMANTIC,
  A.RUNABLE = B.RUNABLE,
  A.PARENTID = B.PARENTID,
  A.ROLENAME = B.ROLENAME,
  A.FRONTEND = B.FRONTEND,
  A.USEARC = B.USEARC;
COMMIT;
