Begin
MERGE INTO BARS.SPPI A USING
 (SELECT
  'Так' as SPPI_ID,
  'Відповідність критерію SPPI («Пройдено»)' as SPPI_NAME
  FROM DUAL) B
ON (A.SPPI_ID = B.SPPI_ID)
WHEN NOT MATCHED THEN 
INSERT (
  SPPI_ID, SPPI_NAME)
VALUES (
  B.SPPI_ID, B.SPPI_NAME)
WHEN MATCHED THEN
UPDATE SET 
  A.SPPI_NAME = B.SPPI_NAME;

MERGE INTO BARS.SPPI A USING
 (SELECT
  'Ні' as SPPI_ID,
  'Відповідність критерію SPPI («Не пройдено»)' as SPPI_NAME
  FROM DUAL) B
ON (A.SPPI_ID = B.SPPI_ID)
WHEN NOT MATCHED THEN 
INSERT (
  SPPI_ID, SPPI_NAME)
VALUES (
  B.SPPI_ID, B.SPPI_NAME)
WHEN MATCHED THEN
UPDATE SET 
  A.SPPI_NAME = B.SPPI_NAME;
end;
/
COMMIT;
