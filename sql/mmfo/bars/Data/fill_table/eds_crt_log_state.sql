MERGE INTO EDS_CRT_LOG_STATE A USING
 (SELECT
  -1 as ID,
  'Новий запит' as NAME,
  'ST_REQUEST_NEW' as STATE
  FROM DUAL) B
ON (A.ID = B.ID)
WHEN NOT MATCHED THEN 
INSERT (
  ID, NAME, STATE)
VALUES (
  B.ID, B.NAME, B.STATE)
WHEN MATCHED THEN
UPDATE SET 
  A.NAME = B.NAME,
  A.STATE = B.STATE;

