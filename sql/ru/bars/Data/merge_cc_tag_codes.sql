MERGE INTO BARS.CC_TAG_CODES A USING
 (SELECT
  'SPEC' as CODE,
  'Бюро Кредит.Історій(Спец)' as NAME,
  2 as ORD
  FROM DUAL) B
ON (A.CODE = B.CODE)
WHEN NOT MATCHED THEN 
INSERT (
  CODE, NAME, ORD)
VALUES (
  B.CODE, B.NAME, B.ORD)
WHEN MATCHED THEN
UPDATE SET 
  A.NAME = B.NAME,
  A.ORD = B.ORD;

COMMIT;
/
