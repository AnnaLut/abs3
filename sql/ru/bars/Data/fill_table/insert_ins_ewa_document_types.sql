 (SELECT
  'PASSPORT' as ID,
  'Паспорт' as NAME,
  1 as EXT_ID
  FROM DUAL) B
ON (A.ID = B.ID)
WHEN NOT MATCHED THEN 
INSERT (
  ID, NAME, EXT_ID)
VALUES (
  B.ID, B.NAME, B.EXT_ID)
WHEN MATCHED THEN
